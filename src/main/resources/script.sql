CREATE TABLE Client (
                        CodeCli VARCHAR(10) PRIMARY KEY,
                        Societe VARCHAR(100),
                        Contact VARCHAR(100),
                        Fonction VARCHAR(100),
                        Adresse VARCHAR(150),
                        Ville VARCHAR(50),
                        CodePostal VARCHAR(10),
                        Pays VARCHAR(50),
                        Telephone VARCHAR(30),
                        Fax VARCHAR(30)
);

